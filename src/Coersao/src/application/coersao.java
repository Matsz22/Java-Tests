package Coersao.src.application;

public class coersao {

    public static void main(String[] args) {

        int i;

        for(i = 0; i < 5; i++) {
            System.out.println(i + " /3: " + i / 3);
            System.out.println(i + " /3 com resto " + (double) i / 3);
            System.out.println();
        }
    }

}
